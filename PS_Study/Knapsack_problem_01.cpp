#include<stdio.h>
#include<string.h>
#define MAX(a,b) ((a)>(b)?(a):(b))
struct P{
	int weight,cost;
};
int n,w;
P d[110];
int f[110][10010];
int back(int i, int w){
	if(f[i][w]>=0) return f[i][w];
	int rtn=0;
	if(i>=n) rtn=0;
	if(w<d[i].cost) rtn=back(i+1,w);
	return rtn=MAX(back(i+1,w), d[i].cost+back(i+1,w-d[i].weight));	
}
int main(){
	scanf("%d%d",&n,&w);
	for(int i=0;i<110;i++)
		for(int k=0;k<10010;k++)
			f[i][k]=-1;
	for(int i=0;i<n;i++)
		scanf("%d%d",&d[i].weight,&d[i].cost);	
	printf("%d",back(0,w));
	return 0;
}
